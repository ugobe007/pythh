#!/usr/bin/env node
/**
 * Compare remote Postgres migration history (supabase_migrations.schema_migrations)
 * with local supabase/migrations/*.sql filenames and print repair hints for `supabase db push`.
 *
 * Requires direct Postgres (not PostgREST):
 *   DATABASE_URL or SUPABASE_DB_URL — pooler or direct connection string with password.
 *
 * Usage:
 *   node scripts/report-supabase-migration-drift.js
 *   node scripts/report-supabase-migration-drift.js --print-repair-reverted
 *   node scripts/report-supabase-migration-drift.js --write-repair-reverted scripts/sql/run_phantom_migration_repair.sh
 *
 * Do not copy `supabase db pull`’s suggested `repair --status applied 20260119`-style lines blindly:
 * those short versions come from ambiguous local filenames (same digit prefix). Use this script’s output.
 *
 * After marking remote phantom rows reverted, run: npx supabase db push
 * (from repo root, linked project). Always snapshot/backup before bulk repair.
 */
'use strict';

const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '..', '.env'), quiet: true });
const fs   = require('fs');
const { Client } = require('pg');

const MIGRATIONS_DIR = path.join(__dirname, '..', 'supabase', 'migrations');

/** Same as run-data-quality-runbook.js — pooler TLS without sslmode=no-verify can fail chain verify. */
function massageConnectionStringForNodePg(connectionString) {
  const s = String(connectionString || '');
  const v = String(process.env.DATABASE_SSL || '').toLowerCase();
  if (v === 'false' || v === '0' || v === 'no') return s;

  const isSupabase = /supabase\.com/i.test(s) || /\.supabase\.co/i.test(s);
  const relax = v === 'true' || v === '1' || v === 'yes' || isSupabase;
  if (!relax) return s;

  if (/sslmode=no-verify/i.test(s)) return s;
  if (/sslmode=/i.test(s)) {
    return s.replace(/sslmode=[^&]*/i, 'sslmode=no-verify');
  }
  return s.includes('?') ? `${s}&sslmode=no-verify` : `${s}?sslmode=no-verify`;
}

function parseArgs() {
  const wIdx = process.argv.indexOf('--write-repair-reverted');
  let writeRepairPath = null;
  if (wIdx !== -1) {
    const next = process.argv[wIdx + 1];
    writeRepairPath = next && !next.startsWith('--')
      ? next
      : path.join(__dirname, 'sql', 'run_phantom_migration_repair.sh');
  }
  return {
    printRepairReverted: process.argv.includes('--print-repair-reverted'),
    printRepairApplied:  process.argv.includes('--print-repair-applied'),
    writeRepairPath,
  };
}

/**
 * Migration version = digits before first `_` (Supabase: "<timestamp>_name.sql").
 * Prefer 14-digit prefix when present so remote rows like 20251216080356 match local files.
 */
function versionFromFilename(name) {
  const m14 = name.match(/^(\d{14})_/);
  if (m14) return m14[1];
  const m = name.match(/^(\d+)_/);
  return m ? m[1] : null;
}

function loadLocalVersions() {
  const versions     = new Set();
  const versionSources = new Map();
  const files        = fs.readdirSync(MIGRATIONS_DIR).filter(f => f.endsWith('.sql'));
  const noVersion    = [];
  for (const f of files) {
    const v = versionFromFilename(f);
    if (v) {
      versions.add(v);
      if (!versionSources.has(v)) versionSources.set(v, []);
      versionSources.get(v).push(f);
    } else {
      noVersion.push(f);
    }
  }
  const collisions = [...versionSources.entries()].filter(([, arr]) => arr.length > 1);
  return { versions, files, noVersion, collisions };
}

function chunk(arr, size) {
  const out = [];
  for (let i = 0; i < arr.length; i += size) out.push(arr.slice(i, i + size));
  return out;
}

function printRepairCommand(status, versions) {
  if (!versions.length) {
    console.log(`(no versions for repair --status ${status})`);
    return;
  }
  const pieces = chunk(versions.sort(), 40);
  console.log(`\n# ${versions.length} version(s) — run each line from repo root (may hit shell length limits; chunks of 40):\n`);
  for (const part of pieces) {
    const args = part.map(v => JSON.stringify(v)).join(' ');
    console.log(`npx supabase migration repair --status ${status} ${args}`);
  }
}

function writeRepairRevertedScript(outPath, remoteOnlySorted) {
  const lines = [
    '#!/usr/bin/env bash',
    '# Phantom remote migration rows → mark reverted so `npx supabase db push` can proceed.',
    '# Generated by: node scripts/report-supabase-migration-drift.js --write-repair-reverted <this-file>',
    '# Run from repo root: bash ' + path.relative(process.cwd(), outPath),
    'set -euo pipefail',
    'ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"',
    'cd "$ROOT"',
    '',
  ];
  const sorted = [...remoteOnlySorted].sort();
  for (const part of chunk(sorted, 30)) {
    const args = part.map(v => `'${String(v).replace(/'/g, "'\\''")}'`).join(' ');
    lines.push(`npx supabase migration repair --status reverted ${args}`);
  }
  fs.writeFileSync(outPath, lines.join('\n') + '\n', 'utf8');
  try {
    fs.chmodSync(outPath, 0o755);
  } catch { /* windows / sandbox */ }
  console.log(`\nWrote ${remoteOnlySorted.length} repair command(s) (chunked) to: ${outPath}`);
}

async function main() {
  const flags = parseArgs();
  const dbUrl = process.env.DATABASE_URL || process.env.SUPABASE_DB_URL;
  if (!dbUrl) {
    console.error('Missing DATABASE_URL or SUPABASE_DB_URL (direct Postgres URI).');
    process.exit(1);
  }

  const { versions: local, noVersion, collisions } = loadLocalVersions();
  console.log(`Local migration files: ${fs.readdirSync(MIGRATIONS_DIR).filter(f => f.endsWith('.sql')).length}`);
  console.log(`Distinct migration versions parsed (from filenames): ${local.size}`);
  if (collisions.length) {
    console.log(`\n⚠️  ${collisions.length} version string(s) map to multiple files — rename to unique YYYYMMDDHHMMSS_ prefixes or history will stay ambiguous:`);
    for (const [ver, fl] of collisions.slice(0, 12)) {
      console.log(`   ${ver} → ${fl.join(', ')}`);
    }
    if (collisions.length > 12) console.log(`   … +${collisions.length - 12} more`);
  }
  if (noVersion.length) {
    console.log(`\nFiles with no leading-digit version (CLI often skips these for history): ${noVersion.length}`);
    console.log(noVersion.slice(0, 25).join('\n') + (noVersion.length > 25 ? `\n… +${noVersion.length - 25} more` : ''));
  }

  const client = new Client({ connectionString: massageConnectionStringForNodePg(dbUrl) });
  let remoteRows;
  try {
    await client.connect();
    const r = await client.query(
      `SELECT version FROM supabase_migrations.schema_migrations ORDER BY version`
    );
    remoteRows = r.rows;
  } catch (e) {
    console.error('\nPostgres error:', e.message);
    if (/password must be a string/i.test(e.message)) {
      console.error(
        'Hint: password segment empty or invalid in URI. URL-encode special characters in the password (e.g. @ → %40, # → %23).'
      );
    }
    if (/SASL authentication failed|password authentication failed/i.test(e.message)) {
      console.error(
        'Hint: server rejected credentials. After a DB password reset, paste the new URI from Supabase → Settings → Database.\n' +
        '  • Direct: host db.<ref>.supabase.co, port 5432, user postgres.\n' +
        '  • Transaction pooler: port 6543 on *.pooler.supabase.com, user postgres.<ref> — not user postgres on db.*:6543.\n' +
        'If the password has &, ?, #, etc., percent-encode it in the URL.'
      );
    }
    if (e.code === 'XX000' || /Tenant or user not found/i.test(e.message)) {
      console.error(
        'Hint: "Tenant or user not found" usually means the pooler hostname or user segment does not match this project (postgres.<project-ref>), or the password is wrong. Copy the exact "URI" from Supabase → Settings → Database.'
      );
    }
    if (/schema_migrations|permission denied/i.test(e.message)) {
      console.error('Hint: use the database password from the same project; role must be able to read supabase_migrations.schema_migrations.');
    }
    process.exit(1);
  } finally {
    try {
      await client.end();
    } catch {
      /* ignore */
    }
  }

  const remote = new Set(remoteRows.map(row => String(row.version)));

  const remoteOnly = [...remote].filter(v => !local.has(v)).sort();
  const localOnly  = [...local].filter(v => !remote.has(v)).sort();

  console.log('\n── Remote rows (history) ──');
  console.log(`Count: ${remote.size}`);

  console.log('\n── Drift summary ──');
  console.log(`Remote-only (in DB, not matched by local leading-digit parse): ${remoteOnly.length}`);
  console.log(`Local-only (in repo, not in DB history): ${localOnly.length}`);

  if (remoteOnly.length && remoteOnly.length <= 60) {
    console.log('\nRemote-only versions:');
    remoteOnly.forEach(v => console.log(`  ${v}`));
  } else if (remoteOnly.length) {
    console.log('\nRemote-only (first 40):');
    remoteOnly.slice(0, 40).forEach(v => console.log(`  ${v}`));
    console.log(`  … +${remoteOnly.length - 40} more`);
  }

  if (localOnly.length && localOnly.length <= 40) {
    console.log('\nLocal-only versions:');
    localOnly.forEach(v => console.log(`  ${v}`));
  } else if (localOnly.length) {
    console.log('\nLocal-only (first 30):');
    localOnly.slice(0, 30).forEach(v => console.log(`  ${v}`));
    console.log(`  … +${localOnly.length - 30} more`);
  }

  console.log('\n── Suggested workflow ──');
  console.log('1. Backup / confirm staging.');
  console.log('2. If remote has versions that were never in this repo (CI/other machine), mark them reverted:');
  console.log('   node scripts/report-supabase-migration-drift.js --print-repair-reverted');
  console.log('3. If this repo has migrations DB never recorded (fresh branch), mark applied after you applied SQL by hand:');
  console.log('   node scripts/report-supabase-migration-drift.js --print-repair-applied');
  console.log('4. npx supabase db push');
  console.log('\n── DATABASE_URL ──');
  console.log('Supabase Dashboard → Project Settings → Database → URI (direct or pooler).');
  console.log('Add to .env as DATABASE_URL=postgresql://...  (this script does not use the anon API key.)');

  if (flags.printRepairReverted) printRepairCommand('reverted', remoteOnly);
  if (flags.printRepairApplied) printRepairCommand('applied', localOnly);

  if (flags.writeRepairPath) {
    const out = path.isAbsolute(flags.writeRepairPath)
      ? flags.writeRepairPath
      : path.join(process.cwd(), flags.writeRepairPath);
    if (!remoteOnly.length) {
      console.log('\n(no remote-only versions — not writing repair script)');
    } else {
      writeRepairRevertedScript(out, remoteOnly);
    }
  }
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
